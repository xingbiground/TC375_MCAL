typedef struct udp_pcb udpPcb;
typedef struct pbuf pBuf;               /* Define a more convenient type                                            */

/***********************************************************************************************************************
 *  LOCAL DATA PROTOTYPES
 **********************************************************************************************************************/
udpPcb *udpTestPcb;
pBuf *udpBuffer;


